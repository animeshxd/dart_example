void main(){
	var hello = 'hello';
	print(hello.toUpperCase()); //uppercase
	var HELLO = 'HELLO';
	print(HELLO.toLowerCase()); //lowercase

	var five = '5';
	print(num.parse(five));
	print(int.parse(five));
	//print(double.parse(five)); // invalid


	var fivepointsix = '5.6';
	print(num.parse(fivepointsix));
	print(double.parse(fivepointsix));
	// print(int.parse(fivepointsix)); //invalid

	var intfive = 5;


	print(intfive.toString()); //5 is string
	print(intfive.toDouble());
	print(intfive.toInt());
	

	var doublefive = 5.6;
	print(doublefive.toInt());
	print(doublefive.toDouble());
	print(doublefive.toString());
	



}
